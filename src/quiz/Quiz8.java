package quiz;

public class Quiz8 {

	public static void main(String[] args) {
		
		int i = 10;
		float f = 2.0f;
		
		int a = i + (int)f;
//		int a = (int) ( i + f ); 얘도 가능
		System.out.println(a);
		
		int b = i - (int)f;
		System.out.println(b);
		
		int c = i * (int)f;
		System.out.println(c);
		
		int d = i / (int)f;
		System.out.println(d);

	}

}
